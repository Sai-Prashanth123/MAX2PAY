export const formatCurrency = (amount) => {
  if (amount === null || amount === undefined) return '$0.00';
  return `$${Number(amount).toFixed(2).replace(/\B(?=(\d{3})+(?!\d))/g, ',')}`;
};

export const formatDate = (date) => {
  if (!date) return '-';
  const d = new Date(date);
  const month = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  const year = d.getFullYear();
  return `${month}/${day}/${year}`;
};

export const formatPhone = (phone) => {
  if (!phone) return '';
  const cleaned = phone.replace(/\D/g, '');
  if (cleaned.length === 10) {
    return `(${cleaned.slice(0, 3)}) ${cleaned.slice(3, 6)}-${cleaned.slice(6)}`;
  }
  return phone;
};

export const validateUSPhone = (phone) => {
  const cleaned = phone.replace(/\D/g, '');
  if (cleaned.length !== 10) {
    return 'Please enter a valid 10-digit US phone number';
  }
  return '';
};

export const validateUSZipCode = (zipCode) => {
  const cleaned = zipCode.replace(/\D/g, '');
  if (cleaned.length !== 5 && cleaned.length !== 9) {
    return 'Please enter a valid 5 or 9-digit US zip code';
  }
  return '';
};

export const formatZipCode = (zipCode) => {
  if (!zipCode) return '';
  const cleaned = zipCode.replace(/\D/g, '');
  if (cleaned.length === 9) {
    return `${cleaned.slice(0, 5)}-${cleaned.slice(5)}`;
  }
  return cleaned;
};
